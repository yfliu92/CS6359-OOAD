function getValues(pre,sday,eday){
	var prefix = document.form1.prefix;
	var i=0;
	for(var [key,value] of myMap){
		prefix[i+1] = new Option(value,key);
		if(key==pre) prefix[i+1].selected=true;
		i++;
	}
	
	sm = sday.split("-")[1];
	sd = sday.split("-")[2];
	em = eday.split("-")[1];
	ed = eday.split("-")[2];
	var mmap = new Map();
	mmap.set(1,"January")
	mmap.set(2,"February")
	mmap.set(3,"March")
	mmap.set(4,"April")
	mmap.set(5,"May")
	mmap.set(6,"June")
	mmap.set(7,"July")
	mmap.set(8,"August")
	mmap.set(9,"September")
	mmap.set(10,"October")
	mmap.set(11,"November")
	mmap.set(12,"December")
	var smonth = document.form1.smonth;
	var emonth = document.form1.emonth;
	var j=0;
	for(var [key,value] of mmap){
		smonth[j+1] = new Option(value,key);
		if(sm==key) smonth[j+1].selected=true;
		emonth[j+1] = new Option(value,key);
		if(em==key) emonth[j+1].selected=true;
		j++;
		}
	
	var year = document.form1.year.value;
	var m1 = new Set(["01","03","05","07","08","10","12"]);
	var m2 = new Set(["04","06","09","11"]);
	var day1 = document.form1.sday;
	if(m1.has(sm)){
		for(var i=1;i<=31;i++){
			day1[i] = new Option(i,i);
			if(sd==i) day1[i].selected=true;
		}
	}
	else if(m2.has(sm)){
		for(var i=1;i<=30;i++){
			day1[i] = new Option(i,i);
			if(sd==i) day1[i].selected=true;
		}
	}
	else{
		var x = 28;
		if(year%4==0&&year%100!=0 || year%400==0){
			x = 29;
		}
		for(var i=1;i<=x;i++){
			day1[i] = new Option(i,i);
			if(sd==i) day1[i].selected=true;
		}
	}
	
	var day2 = document.form1.eday;
	
	if(m1.has(em)){
		for(var i=1;i<=31;i++){
			day2[i] = new Option(i,i);
			if(ed==i) day2[i].selected=true;
		}
	}
	else if(m2.has(em)){
		for(var i=1;i<=30;i++){
			day2[i] = new Option(i,i);
			if(ed==i) day2[i].selected=true;
		}
	}
	else{
		var x = 28;
		if(year%4==0&&year%100!=0 || year%400==0){
			x = 29;
		}
		for(var i=1;i<=x;i++){
			day2[i] = new Option(i,i);
			if(ed==i) day2[i].selected=true;
		}
	}
}

