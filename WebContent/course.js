function getDay(x){
	var m1 = new Set(["1","3","5","7","8","10","12"]);
	var m2 = new Set(["4","6","9","11"]);
	var year;
	var month;
	var day;
	if(x==1){
		year = document.form1.syear.value;
		month = document.form1.smonth.value;
		day = document.form1.sday;
	}
	else{
		year = document.form1.eyear.value;
		month = document.form1.emonth.value;
		day = document.form1.eday;
	}
	//alert(m1.has(month))
	if(m1.has(month)){
		day.options.length=1;
		for(var i=1;i<=31;i++){
			day[i] = new Option(i,i);
		}
	}
	else if(m2.has(month)){
		day.options.length=1;
		for(var i=1;i<=30;i++){
			day[i] = new Option(i,i);
		}
	}
	else{
		day.options.length=1;
		var x = 28;
		if(year%4==0&&year%100!=0 || year%400==0){
			x = 29;
		}
		for(var i=1;i<=x;i++){
			day[i] = new Option(i,i);
		}
	}
}


